CREATE PROCEDURE NETDESLIB.USP_GETPRODUCTOBYID
(
	@IDPRODUCTO VARCHAR(36)
)
LANGUAGE SQL

RESULT SETS 1

BEGIN

DECLARE C1 CURSOR FOR
SELECT IDPRODUCTO
		,CODIGO
		,DESCRIPCION
		,PRECIOUNITARIO
	FROM NETDESLIB.PRODUCTO
	WHERE IDPRODUCTO = @IDPRODUCTO
	ORDER BY CODIGO;
	
	OPEN C1;

END